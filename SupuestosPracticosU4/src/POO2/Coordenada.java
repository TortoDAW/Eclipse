package POO2;

public class Coordenada {
	private double x;
	private double y;
	
	public Coordenada(double x, double y) {
		this.getX();
		this.getY();
	}
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
}
